package personInheritence;

public class Person {
    public String name;
    public String gender;
    public String address;
    public int age;

    public Person() {

    }
    public Person(String name, String gender, String address, int age) {
        this.name = name;
        this.gender = gender;
        this.address = address;
        this.age = age;
    }
}
